import streamlit as st
import pandas as pd

from utils.prediction import predict_heart
from utils.report_generator import generate_report

st.set_page_config(page_title="Heart Disease Prediction | CareRisk AI", page_icon="❤️", layout="wide")


def result_card(result):
    color = result["risk_color"]
    st.markdown(
        f"""
        <div style="padding:1.4rem;border-radius:18px;border-left:8px solid {color};background:#ffffff;box-shadow:0 8px 25px rgba(15,23,42,0.08);">
            <h2 style="margin:0;color:{color};">{result['risk_level']}</h2>
            <p style="font-size:1.1rem;margin-top:0.5rem;">Risk Probability: <b>{result['probability_percent']}%</b></p>
            <p>Model Used: <b>{result['model_used']}</b></p>
        </div>
        """,
        unsafe_allow_html=True,
    )


st.title("❤️ Heart Disease Risk Prediction")
st.caption("Enter values matching the Kaggle johnsmith88 Heart Disease dataset.")
st.warning("This is not medical advice. Use this project for learning and portfolio demonstration only.")

with st.form("heart_form"):
    c1, c2, c3 = st.columns(3)

    with c1:
        age = st.number_input("age", min_value=1, max_value=120, value=50)
        sex = st.selectbox("sex", options=[0, 1], format_func=lambda x: "Female (0)" if x == 0 else "Male (1)")
        cp = st.selectbox("cp - chest pain type", options=[0, 1, 2, 3])
        trestbps = st.number_input("trestbps - resting BP", min_value=60, max_value=250, value=130)
        chol = st.number_input("chol - cholesterol", min_value=80, max_value=700, value=240)

    with c2:
        fbs = st.selectbox("fbs - fasting blood sugar > 120", options=[0, 1])
        restecg = st.selectbox("restecg", options=[0, 1, 2])
        thalach = st.number_input("thalach - max heart rate", min_value=60, max_value=250, value=150)
        exang = st.selectbox("exang - exercise angina", options=[0, 1])

    with c3:
        oldpeak = st.number_input("oldpeak", min_value=0.0, max_value=10.0, value=1.0, step=0.1)
        slope = st.selectbox("slope", options=[0, 1, 2])
        ca = st.selectbox("ca - major vessels", options=[0, 1, 2, 3, 4])
        thal = st.selectbox("thal", options=[0, 1, 2, 3])

    submitted = st.form_submit_button("🔍 Predict Heart Disease Risk", use_container_width=True)

if submitted:
    input_data = {
        "age": age,
        "sex": sex,
        "cp": cp,
        "trestbps": trestbps,
        "chol": chol,
        "fbs": fbs,
        "restecg": restecg,
        "thalach": thalach,
        "exang": exang,
        "oldpeak": oldpeak,
        "slope": slope,
        "ca": ca,
        "thal": thal,
    }

    result = predict_heart(input_data)

    if "error" in result:
        st.warning(result["error"])
        st.info("Run: `python -m utils.model_training --disease heart` after adding `data/heart.csv`.")
    else:
        result_card(result)
        st.divider()

        col1, col2 = st.columns([1, 1])
        with col1:
            st.subheader("📌 Important Risk Factors")
            factors = result.get("top_factors", [])
            if factors:
                factor_df = pd.DataFrame(factors)
                factor_df["importance"] = factor_df["importance"].round(4)
                st.dataframe(factor_df, use_container_width=True, hide_index=True)
            else:
                st.info("Feature importance is not available for this model.")

        with col2:
            st.subheader("📄 Download Report")
            pdf_bytes = generate_report(
                patient_data=input_data,
                disease_type="Heart Disease",
                prediction=result["prediction"],
                probability=result["probability"],
                risk_level=result["risk_level"],
                top_factors=result["top_factors"],
            )
            st.download_button(
                label="Download PDF Report",
                data=pdf_bytes,
                file_name="heart_disease_risk_report.pdf",
                mime="application/pdf",
                use_container_width=True,
            )

        st.error("⚠️ This prediction is generated by an ML model and is not a medical diagnosis.")
