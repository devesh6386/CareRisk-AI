import streamlit as st
import pandas as pd

from utils.prediction import predict_stroke
from utils.report_generator import generate_report

st.set_page_config(page_title="Stroke Prediction | CareRisk AI", page_icon="🧠", layout="wide")


def result_card(result):
    color = result["risk_color"]
    st.markdown(
        f"""
        <div style="padding:1.4rem;border-radius:18px;border-left:8px solid {color};background:#ffffff;box-shadow:0 8px 25px rgba(15,23,42,0.08);">
            <h2 style="margin:0;color:{color};">{result['risk_level']}</h2>
            <p style="font-size:1.1rem;margin-top:0.5rem;">Risk Probability: <b>{result['probability_percent']}%</b></p>
            <p>Model Used: <b>{result['model_used']}</b></p>
        </div>
        """,
        unsafe_allow_html=True,
    )


st.title("🧠 Stroke Risk Prediction")
st.caption("Enter values matching the Kaggle fedesoriano Stroke Prediction dataset.")
st.warning("This is not medical advice. Use this project for learning and portfolio demonstration only.")

with st.form("stroke_form"):
    c1, c2 = st.columns(2)

    with c1:
        gender = st.selectbox("gender", options=["Male", "Female", "Other"])
        age = st.number_input("age", min_value=0.0, max_value=120.0, value=45.0, step=1.0)
        hypertension = st.selectbox("hypertension", options=[0, 1])
        heart_disease = st.selectbox("heart_disease", options=[0, 1])
        ever_married = st.selectbox("ever_married", options=["Yes", "No"])

    with c2:
        work_type = st.selectbox("work_type", options=["Private", "Self-employed", "Govt_job", "children", "Never_worked"])
        Residence_type = st.selectbox("Residence_type", options=["Urban", "Rural"])
        avg_glucose_level = st.number_input("avg_glucose_level", min_value=40.0, max_value=350.0, value=100.0, step=0.1)
        bmi = st.number_input("bmi", min_value=5.0, max_value=100.0, value=25.0, step=0.1)
        smoking_status = st.selectbox(
            "smoking_status",
            options=["formerly smoked", "never smoked", "smokes", "Unknown"],
        )

    submitted = st.form_submit_button("🔍 Predict Stroke Risk", use_container_width=True)

if submitted:
    input_data = {
        "gender": gender,
        "age": age,
        "hypertension": hypertension,
        "heart_disease": heart_disease,
        "ever_married": ever_married,
        "work_type": work_type,
        "Residence_type": Residence_type,
        "avg_glucose_level": avg_glucose_level,
        "bmi": bmi,
        "smoking_status": smoking_status,
    }

    result = predict_stroke(input_data)

    if "error" in result:
        st.warning(result["error"])
        st.info("Run: `python -m utils.model_training --disease stroke` after adding `data/stroke.csv`.")
    else:
        result_card(result)
        st.divider()

        col1, col2 = st.columns([1, 1])
        with col1:
            st.subheader("📌 Important Risk Factors")
            factors = result.get("top_factors", [])
            if factors:
                factor_df = pd.DataFrame(factors)
                factor_df["importance"] = factor_df["importance"].round(4)
                st.dataframe(factor_df, use_container_width=True, hide_index=True)
            else:
                st.info("Feature importance is not available for this model.")

        with col2:
            st.subheader("📄 Download Report")
            pdf_bytes = generate_report(
                patient_data=input_data,
                disease_type="Stroke",
                prediction=result["prediction"],
                probability=result["probability"],
                risk_level=result["risk_level"],
                top_factors=result["top_factors"],
            )
            st.download_button(
                label="Download PDF Report",
                data=pdf_bytes,
                file_name="stroke_risk_report.pdf",
                mime="application/pdf",
                use_container_width=True,
            )

        st.error("⚠️ This prediction is generated by an ML model and is not a medical diagnosis.")
