import streamlit as st
import pandas as pd

from utils.prediction import predict_diabetes
from utils.report_generator import generate_report

st.set_page_config(page_title="Diabetes Prediction | CareRisk AI", page_icon="🩸", layout="wide")


def result_card(result):
    color = result["risk_color"]
    st.markdown(
        f"""
        <div style="padding:1.4rem;border-radius:18px;border-left:8px solid {color};background:#ffffff;box-shadow:0 8px 25px rgba(15,23,42,0.08);">
            <h2 style="margin:0;color:{color};">{result['risk_level']}</h2>
            <p style="font-size:1.1rem;margin-top:0.5rem;">Risk Probability: <b>{result['probability_percent']}%</b></p>
            <p>Model Used: <b>{result['model_used']}</b></p>
        </div>
        """,
        unsafe_allow_html=True,
    )


st.title("🩸 Diabetes Risk Prediction")
st.caption("Enter clinical values from the Pima Indians Diabetes feature set.")
st.warning("This is not medical advice. Use this project for learning and portfolio demonstration only.")

with st.form("diabetes_form"):
    c1, c2, c3, c4 = st.columns(4)
    with c1:
        Pregnancies = st.number_input("Pregnancies", min_value=0, max_value=20, value=1)
        Glucose = st.number_input("Glucose", min_value=0.0, max_value=300.0, value=120.0)
    with c2:
        BloodPressure = st.number_input("BloodPressure", min_value=0.0, max_value=200.0, value=70.0)
        SkinThickness = st.number_input("SkinThickness", min_value=0.0, max_value=100.0, value=20.0)
    with c3:
        Insulin = st.number_input("Insulin", min_value=0.0, max_value=900.0, value=80.0)
        BMI = st.number_input("BMI", min_value=0.0, max_value=80.0, value=28.0)
    with c4:
        DiabetesPedigreeFunction = st.number_input("DiabetesPedigreeFunction", min_value=0.0, max_value=3.0, value=0.45)
        Age = st.number_input("Age", min_value=1, max_value=120, value=35)

    submitted = st.form_submit_button("🔍 Predict Diabetes Risk", use_container_width=True)

if submitted:
    input_data = {
        "Pregnancies": Pregnancies,
        "Glucose": Glucose,
        "BloodPressure": BloodPressure,
        "SkinThickness": SkinThickness,
        "Insulin": Insulin,
        "BMI": BMI,
        "DiabetesPedigreeFunction": DiabetesPedigreeFunction,
        "Age": Age,
    }

    result = predict_diabetes(input_data)

    if "error" in result:
        st.warning(result["error"])
        st.info("Run: `python -m utils.model_training --disease diabetes` after adding `data/diabetes.csv`.")
    else:
        result_card(result)
        st.divider()

        col1, col2 = st.columns([1, 1])
        with col1:
            st.subheader("📌 Important Risk Factors")
            factors = result.get("top_factors", [])
            if factors:
                factor_df = pd.DataFrame(factors)
                factor_df["importance"] = factor_df["importance"].round(4)
                st.dataframe(factor_df, use_container_width=True, hide_index=True)
            else:
                st.info("Feature importance is not available for this model.")

        with col2:
            st.subheader("📄 Download Report")
            pdf_bytes = generate_report(
                patient_data=input_data,
                disease_type="Diabetes",
                prediction=result["prediction"],
                probability=result["probability"],
                risk_level=result["risk_level"],
                top_factors=result["top_factors"],
            )
            st.download_button(
                label="Download PDF Report",
                data=pdf_bytes,
                file_name="diabetes_risk_report.pdf",
                mime="application/pdf",
                use_container_width=True,
            )

        st.error("⚠️ This prediction is generated by an ML model and is not a medical diagnosis.")
